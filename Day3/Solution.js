
// Challenge 1

function solve(x) {
	const strEach = x.split("\n")
    let position = 0,
        concat = 2,
        treeCount = 0

    strEach.forEach((str, i) => {
        let pattern = str
        if (position > 30) {
            pattern = str.repeat(concat)
            concat++
        }
        let map = pattern.split("")
        if (map[position] === "#") {
            treeCount++
        }
        position += 3
    })
    return treeCount
}

// Challenge 2

function solve1(x) {
   	const strEach = x.split("\n")
   	// slope[tree count, position]
    let slope1 = [0, 0],
        slope2 = [0, 0],
        slope3 = [0, 0],
        slope4 = [0, 0],
        slope5 = [0, 1],
        concat = 2
    strEach.forEach((str, i) => {
        let pattern = str
        if (slope2[1] > 21) {
            pattern = str.repeat(concat)
            concat++
        }
        let map = pattern.split("")
        if (map[slope1[1]] === "#") {
            slope1[0]++
        }
        if (map[slope2[1]] === "#") {
            slope2[0]++
        }
        if (map[slope3[1]] === "#") {
            slope3[0]++
        }
        if (map[slope4[1]] === "#") {
            slope4[0]++
        }
        if (i % 2 === 0 && i > 0) {
            if (map[slope5[1]] === "#") {
                slope5[0]++
            }
            slope5[1]++
        }
        slope1[1]++
        slope2[1] += 3
        slope3[1] += 5
        slope4[1] += 7

    })
    return slope1[0] * slope2[0] * slope3[0] * slope4[0] * slope5[0]
}



